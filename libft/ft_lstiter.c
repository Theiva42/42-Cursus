#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	if (!lst)
		return ;
	while (lst != NULL)
	{
		temp = lst -> next;
		f(lst -> content);
		lst = temp;
	}
}
